    private void btnAceptarMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAceptarMouseReleased
        // cuando se suelta el boton
        // Aqui se ejecuta el codigo del boton
        if(!"".equals(fieldNombre.getText()) && !"".equals(fieldCI.getText()) &&
           !"".equals(fieldDireccion.getText()) && !"".equals(fieldTelefono.getText()) &&
           comboNacional.getSelectedIndex()!= 0){
            String nombre = fieldNombre.getText();
            String nacionalidad = (String) comboNacional.getSelectedItem();
            String CI_RIF = fieldCI.getText();
            String direccion = fieldDireccion.getText();
            String telefono = fieldTelefono.getText();
            xmlListado.editarCliente(nombre, nacionalidad, CI_RIF, direccion, telefono);
            
        ventana.setEnabled(true);
        ventana.actualizarTabla();
        dispose();
        }else
            JOptionPane.showMessageDialog(null, "Error, campos vacios o sin seleccionar","Advertencia",2);
    }//GEN-LAST:event_btnAceptarMouseReleased

